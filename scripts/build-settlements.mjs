// scripts/build-settlements.mjs
// Run once: node scripts/build-settlements.mjs
import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dir = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dir, '..')

function getTier(nameAz, typeAz) {
  if (typeAz === 'şəhər') {
    const upper = nameAz.toUpperCase()
    if (upper.includes('BAK') || upper.includes('SUMQ')) return 'metro-city'
    return 'city'
  }
  if (typeAz === 'qəsəbə') return 'town'
  return 'rural'
}

console.log('Reading GeoJSON...')
const raw = readFileSync(
  resolve(root, 'datasets/azrbaycann_hrlri_v_inzibati_razi_vahidlri.geojson'),
  'utf-8'
)
const geojson = JSON.parse(raw)
console.log(`Loaded ${geojson.features.length} features`)

const settlements = []
for (const feature of geojson.features) {
  const coords = feature.geometry?.coordinates
  const props = feature.properties
  if (!coords || !props) continue
  const [lng, lat] = coords
  const nameAz = props.NAME_AZ || ''
  const typeAz = props.TYPE_AZ || ''
  const tier = getTier(nameAz, typeAz)
  settlements.push({ lat: +lat.toFixed(6), lng: +lng.toFixed(6), tier })
}

const tierCounts = settlements.reduce((acc, s) => {
  acc[s.tier] = (acc[s.tier] ?? 0) + 1; return acc
}, {})
console.log('Tier distribution:', tierCounts)

const tsLines = [
  '// AUTO-GENERATED by scripts/build-settlements.mjs — do not edit manually',
  '// Source: datasets/azrbaycann_hrlri_v_inzibati_razi_vahidlri.geojson',
  "import { haversineMetres } from './geo'",
  '',
  "export type UrbanTier = 'metro-city' | 'city' | 'town' | 'rural'",
  '',
  'interface Settlement { lat: number; lng: number; tier: UrbanTier }',
  '',
  'const SETTLEMENTS: Settlement[] = ' + JSON.stringify(settlements),
  '',
  '/** Returns the urban tier of the nearest settlement to the given coordinates. */',
  'export function getUrbanTier(lat: number, lng: number): UrbanTier {',
  '  let best: { distance: number; tier: UrbanTier } | null = null',
  '  for (const s of SETTLEMENTS) {',
  '    const d = haversineMetres(lat, lng, s.lat, s.lng)',
  '    if (!best || d < best.distance) best = { distance: d, tier: s.tier }',
  '  }',
  "  return best?.tier ?? 'rural'",
  '}',
]

writeFileSync(resolve(root, 'lib/settlements.ts'), tsLines.join('\n'), 'utf-8')
const fileSizeKB = Math.round(readFileSync(resolve(root, 'lib/settlements.ts')).length / 1024)
console.log(`Written lib/settlements.ts — ${settlements.length} settlements, ${fileSizeKB} KB`)
