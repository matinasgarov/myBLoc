// scripts/build-metro-data.mjs
// Run once: node scripts/build-metro-data.mjs
import { readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dir = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dir, '..')

// --- Parse pathways.csv ---
const pathwaysRaw = readFileSync(resolve(root, 'datasets/pathways.csv'), 'utf-8')
const pathwaysLines = pathwaysRaw.split('\n').slice(1).filter(l => l.trim())

// Group exits by station AZ name
const exitsByStation = new Map() // nameAz → [{lat, lng}]
for (const line of pathwaysLines) {
  const cols = line.split(';').map(c => c.replace(/^"|"$/g, '').trim())
  const nameAz = cols[0]
  const latInt = parseInt(cols[2])
  const lngInt = parseInt(cols[3])
  if (!nameAz || isNaN(latInt) || isNaN(lngInt)) continue
  const lat = latInt / 1_000_000
  const lng = lngInt / 1_000_000
  if (!exitsByStation.has(nameAz)) exitsByStation.set(nameAz, [])
  exitsByStation.get(nameAz).push({ lat, lng })
}

// --- Parse passenger_2025.csv ---
const passengerRaw = readFileSync(resolve(root, 'datasets/passenger_2025.csv'), 'utf-8')
const passengerLines = passengerRaw.split('\n').slice(1).filter(l => l.trim())

// Accumulate ridership per station name
const ridershipSum = new Map()   // name → total
const ridershipCount = new Map() // name → day count
for (const line of passengerLines) {
  const cols = line.split(';').map(c => c.replace(/^"|"$/g, '').trim())
  const stationName = cols[1]
  const count = parseInt(cols[2])
  if (!stationName || isNaN(count)) continue
  ridershipSum.set(stationName, (ridershipSum.get(stationName) ?? 0) + count)
  ridershipCount.set(stationName, (ridershipCount.get(stationName) ?? 0) + 1)
}

// Average ridership per station
const avgRidership = new Map()
for (const [name, total] of ridershipSum) {
  avgRidership.set(name, Math.round(total / ridershipCount.get(name)))
}

// --- Name fixups: pathways.csv nameAz → passenger_2025.csv name ---
const NAME_FIXUPS = new Map([
  ['28.05.2026 00:00:00', '28 May'],
  ['Şah İsmayıl Xətai', 'Xətai'],
  ['Memar Əcəmi 2', 'Memar Əcəmi-2'],
  ['Koroğlu', 'Koroglu'],
])

// Display name fixups (pathways nameAz → canonical display name)
const DISPLAY_NAME_FIXUPS = new Map([
  ['28.05.2026 00:00:00', '28 May'],
])

// --- Build metro stations array ---
const stations = []
for (const [nameAz, exits] of exitsByStation) {
  const passengerKey = NAME_FIXUPS.get(nameAz) ?? nameAz
  const ridership = avgRidership.get(passengerKey) ?? 0
  const displayName = DISPLAY_NAME_FIXUPS.get(nameAz) ?? nameAz
  stations.push({ nameAz: displayName, exits, dailyRidership: ridership })
}

// Sort by nameAz for stable output
stations.sort((a, b) => a.nameAz.localeCompare(b.nameAz, 'az'))

// --- Write lib/metro-stations.ts ---
const tsLines = [
  '// AUTO-GENERATED by scripts/build-metro-data.mjs — do not edit manually',
  '// Source: datasets/pathways.csv + datasets/passenger_2025.csv',
  "import { haversineMetres } from './geo'",
  '',
  'export interface MetroStation {',
  '  nameAz: string',
  '  exits: Array<{ lat: number; lng: number }>',
  '  dailyRidership: number',
  '}',
  '',
  'export const METRO_STATIONS: MetroStation[] = ' + JSON.stringify(stations, null, 2),
  '',
  '/** Returns nearest metro exit distance + station ridership, or null if >2 000 m away. */',
  'export function getNearestMetro(',
  '  lat: number,',
  '  lng: number,',
  '): { distance: number; ridership: number } | null {',
  '  let best: { distance: number; ridership: number } | null = null',
  '  for (const station of METRO_STATIONS) {',
  '    for (const exit of station.exits) {',
  '      const d = haversineMetres(lat, lng, exit.lat, exit.lng)',
  '      if (!best || d < best.distance) {',
  '        best = { distance: Math.round(d), ridership: station.dailyRidership }',
  '      }',
  '    }',
  '  }',
  '  if (!best || best.distance > 2000) return null',
  '  return best',
  '}',
]
writeFileSync(resolve(root, 'lib/metro-stations.ts'), tsLines.join('\n'), 'utf-8')
console.log(`Written lib/metro-stations.ts — ${stations.length} stations`)
console.log('\nPassenger CSV station names found:')
for (const name of [...avgRidership.keys()].sort()) {
  console.log(`  "${name}": ${avgRidership.get(name)}/day avg`)
}
console.log('\nStations written:')
for (const s of stations) {
  const status = s.dailyRidership === 0 ? ' ⚠ ZERO RIDERSHIP' : ''
  console.log(`  ${s.nameAz}: ${s.dailyRidership}/day avg, ${s.exits.length} exits${status}`)
}
